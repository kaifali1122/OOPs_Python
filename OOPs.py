## Simple car analogy 

# * **Class**: Think of a class like a *car mold* or *blueprint* — it defines the structure of a car (what parts a car will have). Example words you can use: *mold / structure / design*.

# * **Attributes**: These are the *facts* about a car made from the mold — for example **color**, **brand**, **model**, **number of doors**. Attributes store the car's properties.

# * **Method**: A method is a *behavior* or *action* the car can do — like `start()`, `stop()`, `honk()`, or `drive()`. Methods are the car's features or actions.

# * **Object**: An object is the real car you get after using the mold and filling attributes — the actual product. For example: a red Honda city made from the car mold. In simple Hindi-English: *"class = mold, attributes = color/brand, method = kya kar sakta hai, object = jo final car banta hai"*.
